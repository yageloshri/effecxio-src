/**
 * Translates a hyphen-separated icon slug into Hebrew.
 *
 * Each word in the slug is looked up in `ICON_WORDS_HE`.
 * Words that have no translation are kept as-is.
 * The result is joined with spaces.
 *
 * @example
 * translateIconName('arrow-up')    // => 'חץ למעלה'
 * translateIconName('file-text')   // => 'קובץ טקסט'
 * translateIconName('user-check')  // => 'משתמש סימון'
 */
export function translateIconName(slug: string): string {
  return slug
    .split('-')
    .map((word) => ICON_WORDS_HE[word] ?? word)
    .join(' ');
}
